"""function to check if an user is within a geofence"""

def check_geofence(user_location, geofence_center, geofence_radius):
    distance = geodesic(user_location, geofence_center).meters
    return distance <= geofence_radius

def fuzzy_geofence(user_location, geofence_center, base_radius, user_privacy_level):
    # Adjust the geofence radius based on user privacy level and add fuzziness
    adjusted_radius = base_radius * user_privacy_level
    fuzziness = random.uniform(0.8, 1.2)  # Example fuzziness range
    return check_geofence(user_location, geofence_center, adjusted_radius * fuzziness)

"""function to add noise to location data using adaptive differential privacy"""

def adaptive_differential_privacy_noise(actual_location, sensitivity):
    # Adjust epsilon based on sensitivity
    epsilon = 1 / sensitivity
    scale = 1 / epsilon
    noise_x = np.random.laplace(0, scale)
    noise_y = np.random.laplace(0, scale)
    obfuscated_location = (actual_location[0] + noise_x, actual_location[1] + noise_y)
    return obfuscated_location

